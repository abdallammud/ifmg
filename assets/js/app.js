const nextSlideBtn = document.getElementById('nextSlide');
const carouselSlides = document.querySelectorAll('.carousel-slide');

// Initialize
let currentSlide = 0;
let carouselInterval = null;

// Carousel Functions
function showSlide(index) {
    currentSlide = index;
    carouselSlides.forEach((slide, i) => {
        slide.classList.toggle('active', i === index);
    });
    carouselDots.forEach((dot, i) => {
        dot.classList.toggle('active', i === index);
        dot.style.backgroundColor = i === index ? '#C9A227' : 'rgba(255,255,255,0.5)';
        dot.style.width = i === index ? '24px' : '12px';
    });
}

function nextSlide() {
    showSlide((currentSlide + 1) % carouselSlides.length);
}

function prevSlide() {
    showSlide((currentSlide - 1 + carouselSlides.length) % carouselSlides.length);
}

function startCarousel() {
    carouselInterval = setInterval(nextSlide, 6000);
}

function stopCarousel() {
    if (carouselInterval) {
        clearInterval(carouselInterval);
        carouselInterval = null;
    }
}

// Carousel Event Listeners
if (prevSlideBtn && nextSlideBtn) {
    prevSlideBtn.addEventListener('click', () => {
        stopCarousel();
        prevSlide();
        startCarousel();
    });

    nextSlideBtn.addEventListener('click', () => {
        stopCarousel();
        nextSlide();
        startCarousel();
    });
}

carouselDots.forEach((dot, i) => {
    dot.addEventListener('click', () => {
        stopCarousel();
        showSlide(i);
        startCarousel();
    });
});

// Start carousel
if (carouselSlides.length > 0) {
    startCarousel();
}

